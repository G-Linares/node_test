
// dentro de este modulo estan las 4 operaciones

function sumardetodo(num1, num2){
    return num1 + num2;
}
function restardetodo(num1, num2){
    return num1 - num2;
}
function multiplicardetodo(num1, num2){
    return num1 * num2;
}
function dividirdetodo(num1, num2){
    return num1 / num2;
}

module.exports = {sumardetodo, restardetodo, multiplicardetodo, dividirdetodo};